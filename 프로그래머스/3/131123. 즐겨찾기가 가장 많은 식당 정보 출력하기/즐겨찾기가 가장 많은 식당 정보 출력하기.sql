SELECT FOOD_TYPE, REST_ID, REST_NAME, FAVORITES
FROM (
    SELECT DISTINCT FOOD_TYPE, REST_ID, REST_NAME, FAVORITES
    FROM REST_INFO
    ORDER BY FAVORITES DESC
) A
GROUP BY FOOD_TYPE
ORDER BY FOOD_TYPE DESC;

